SELECT s.assetnum
	,s.itemnum
FROM dbo.sparepart AS s
	INNER JOIN dbo.asset AS a
ON s.assetnum = a.assetnum AND s.siteid = a.siteid
WHERE s.siteid = 'FWN' AND status in ('ACTIVE','OPERATING') AND s.itemnum in ('101026','101605','102316','103409','104023','104294','104463','104483','104704','104707','104709','104858','105908','105910','105911','105913','106363','106972','106975','106976','106977','107622','107623','107691','107704','107714','107886','108747','108888','109286','109432','109433','110068','111327','112393','112402','112455','112506','112749','114241','114836','114949','115556','100880R','101795R','104858R','105906A','107682A','108505A')
ORDER BY s.itemnum